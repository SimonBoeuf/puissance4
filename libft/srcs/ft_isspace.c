int		ft_isspace(int c)
{
	unsigned char	t;

	t = (unsigned char) c;
	if (t == '\n' || t == '\t' || t == ' ' || t == '\f' || t == '\r'
		|| t == '\v')
		return (1);
	return (0);
}
